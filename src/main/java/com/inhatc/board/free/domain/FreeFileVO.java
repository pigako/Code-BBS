package com.inhatc.board.free.domain;

public class FreeFileVO {
	private int ff_no;
	private int fb_no;
	private String ff_file_name;
	private String ff_origin_file_name;
	
	public int getFf_no() {
		return ff_no;
	}
	public void setFf_no(int ff_no) {
		this.ff_no = ff_no;
	}
	public int getFb_no() {
		return fb_no;
	}
	public void setFb_no(int fb_no) {
		this.fb_no = fb_no;
	}
	public String getFf_file_name() {
		return ff_file_name;
	}
	public void setFf_file_name(String ff_file_name) {
		this.ff_file_name = ff_file_name;
	}
	public String getFf_origin_file_name() {
		return ff_origin_file_name;
	}
	public void setFf_origin_file_name(String ff_origin_file_name) {
		this.ff_origin_file_name = ff_origin_file_name;
	}
	
	
}
